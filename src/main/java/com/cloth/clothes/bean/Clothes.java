package com.cloth.clothes.bean;


public class Clothes {

  private long id;
  private String name;
  private String feature;
  private String brand;
  private String type;
  private double size;
  private String texture;
  private String couar;
  private String sleeve;
  private String batch;
  private double cost;
  private long number;
  private double profit;
  private String clothesUrl;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getFeature() {
    return feature;
  }

  public void setFeature(String feature) {
    this.feature = feature;
  }


  public String getBrand() {
    return brand;
  }

  public void setBrand(String brand) {
    this.brand = brand;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public double getSize() {
    return size;
  }

  public void setSize(double size) {
    this.size = size;
  }


  public String getTexture() {
    return texture;
  }

  public void setTexture(String texture) {
    this.texture = texture;
  }


  public String getCouar() {
    return couar;
  }

  public void setCouar(String couar) {
    this.couar = couar;
  }


  public String getSleeve() {
    return sleeve;
  }

  public void setSleeve(String sleeve) {
    this.sleeve = sleeve;
  }


  public String getBatch() {
    return batch;
  }

  public void setBatch(String batch) {
    this.batch = batch;
  }


  public double getCost() {
    return cost;
  }

  public void setCost(double cost) {
    this.cost = cost;
  }


  public long getNumber() {
    return number;
  }

  public void setNumber(long number) {
    this.number = number;
  }


  public double getProfit() {
    return profit;
  }

  public void setProfit(double profit) {
    this.profit = profit;
  }

  public String getClothesUrl() {
    return clothesUrl;
  }

  public void setClothesUrl(String clothesUrl) {
    this.clothesUrl = clothesUrl;
  }
}
